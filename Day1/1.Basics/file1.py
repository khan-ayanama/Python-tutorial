# COMMAND LINE TUTORIAL WITH GIT BASH

# 1. Print working directory ----> pwd
# 2. files in folder ----> ls
# 3. To clear terminal ----> clear
# 4. Change folder  -----> cd
# 5. To create folder -----> mkdir "New folder"
# 6. To move in folder  ------> cd "New folder"
# 7. To create new file ------> touch file.txt
# 8. Back in folder -------> cd ..
# 9. To remove file ---->  rm  f.txtile
# 10. To remove folder -----> rm -rf "New folder"
# 11. To run python file ----> python hello.py
# 12. To return in present directory  -----> cd ~
# 13. Rename file -----> mv old_file_name new_file_name
# 14. Move file in another folder ----> mv my_file  ./new_folder
# 15. Copy file ----> cp file1.py  ./new_folder
