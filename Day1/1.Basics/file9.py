# Rules for naming variable

# 1.You can't start with number
# eg:- 1value = 5

# 2.You can start with underscare
# _first_name = 'Ayan'
# print(_first_name)

# 3.You can't use special symbols in variable naming
# last$name = 'khan'
# print(last$name)

